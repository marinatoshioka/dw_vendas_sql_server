MERGE D_EMPRESA D
USING PROCFIT_ST.DBO.VW_D_EMPRESA O ON D.COD_EMPRESA = O.COD_EMPRESA 

WHEN MATCHED THEN UPDATE
SET
NOME = O.NOME, 
NOME_FANTASIA = O.NOME_FANTASIA

WHEN NOT MATCHED BY TARGET THEN

INSERT (
COD_EMPRESA, 
NOME, 
NOME_FANTASIA)

VALUES (
O.COD_EMPRESA, 
O.NOME, 
O.NOME_FANTASIA);


